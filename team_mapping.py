def get_canonical_name(team_name):
    
    # If team name is a key in mappings, it's already in KenPom format
    if team_name in TEAM_MAPPINGS:
        return team_name
        
    # Look through all mappings to find team in variations list
    for kenpom_name, variations in TEAM_MAPPINGS.items():
        if team_name in variations:
            return kenpom_name
            
    # If not found, return original name
    return team_name